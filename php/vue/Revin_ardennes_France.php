            <h3>Préface de Daniel DURBECQ, Maire de Revin.</h3>
            <p>Chères amies, chers amis,
                Le “carnet d’une découverte”, écrit par une femme tombée amoureuse de notre contrée, nous invite à
                découvrir ou à
                redécouvrir notre ville.
                Marie-Simone Poublon relate avec une émotion réelle ses balades et ses découvertes sublimées par la
                poésie des lieux et
                des personnes qu’elle a rencontrées.
                Cette noble démarche de l’auteur n’est pas une fresque historique de Revin, mais une invitation à la
                promenade dans la
                forêt, dans la ville, sur les sentiers et sur les bords de la Meuse.
                À la lecture, si vous prêtez attention, vous entendrez une musique, sentirez des odeurs, reconnaîtrez
                des couleurs qui
                vous rappellent que Revin nous offre ses beautés et son cadre de vie exceptionnels.
                Comme vous le savez, notre équipe municipale œuvre pour le développement du tourisme. Cet ouvrage
                participera à faire
                connaître encore mieux Revin aux visiteurs en espérant leur donner l’envie de s’arrêter plus longtemps
                et de revenir
                souvent.
                Pour vous cher(e)s ami(e)s ces écrits soulignent la chance que nous avons de vivre dans cet
                environnement qui enchante
                tous les voyageurs arrivant pour la première ou la énième fois dans la ville.
                Nos infrastructures municipales et nos associations sont à votre disposition pour vous aider et vous
                conseiller dans vos
                balades.
                Je ne peux que vous inviter à faire comme l’auteur, à suivre son chemin et à vous engager sur la voie de
                la découverte.
                Que ce soit en famille ou seul(e), profitez de cette magie !
                La richesse de notre environnement, de notre faune et de notre flore décrite par l’auteur, est notre
                avenir à tous.
                Nous saluons cette initiative méritante de Marie-Simone Poublon et lui souhaitons de faire encore bien
                d’autres
                découvertes à Revin qu’elle nous livrera, peut-être, un peu plus tard…</p>
